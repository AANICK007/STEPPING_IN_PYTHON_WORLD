# Python script to print first N natural numbers

N = int( input ( "Enter a natural number = " ) )
x = 1

while ( x <= N ) :
    print ( x ) ;
    x += 1 ;
    
    
    
    
# Python script to print first N natural numbers in reverse order

N = int( input ( "Enter a natural number = " ) )
x = N

while ( x >= 1 ) :
    print ( x ) ;
    x -= 1 ;
    